import type { ChapterDef, SpawnEvent } from "../game/types";

function e(
  at: number,
  variant: "walker" | "runner" | "brute" | "crawler",
  x: number,
  opts: Partial<SpawnEvent> = {},
): SpawnEvent {
  return { at, kind: "enemy", variant, x, ...opts };
}

function civ(at: number, x: number): SpawnEvent {
  return { at, kind: "civilian", x, y: 360 };
}

function crate(at: number, x: number): SpawnEvent {
  return { at, kind: "breakable", x, y: 400, z: 0.4 };
}

